const generateButton = document.getElementById("randomize");
const countSelect = document.getElementById("count");
const output = document.querySelector("p");

function generateRandomEpisodes() {
  const count = parseInt(countSelect.value);
  const chosen = [];

  while (chosen.length < count) {
    const randomIndex = Math.floor(Math.random() * words.length);
    const episode = words[randomIndex];
    if (!chosen.includes(episode)) {
      chosen.push(episode);
    }
  }

  let resultText = "";

  if (chosen.length === 1) {
  resultText = `<i>${chosen[0]}</i>`;
} else if (chosen.length === 2) {
  resultText = `<i>${chosen[0]}</i> und <br><i>${chosen[1]}</i>`;
} else if (chosen.length > 2) {
  const allButLast = chosen.slice(0, -1).map(ep => `<i>${ep}</i>`).join(", <br>");
  const last = `<i>${chosen[chosen.length - 1]}</i>`;
  resultText = `${allButLast} und <br>${last}`;
}

  output.innerHTML = ` ${resultText}`;
}

generateButton.addEventListener("click", generateRandomEpisodes);
